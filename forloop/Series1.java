package forloop;

public class Series1 {
	public static void main(String[] args) {
        // Loop to generate the series
        for (int i = 2; i <= 20; i += 2) {
            System.out.println(i);
        }
    }
}
// In this program, the for loop starts from 2 and iterates in steps of 2 (i.e., i += 2), so it generates the even numbers in the range from 2 to 20. The loop continues as long as i is less than or equal to 20. The program prints each even number in the series, resulting in the desired output.








